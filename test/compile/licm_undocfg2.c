typedef int intptr_t;
typedef intptr_t intnt;
static intnt next = 1;
static void process()
{
   char * front_Pos, * back_Pos;
   while(*front_Pos++!=0xd);
   while(front_Pos<=back_Pos){
      const char *front_Pos;
      const char *back_Pos;
      front_Pos="Y"[*back_Pos];
      while(*--back_Pos==0xd);
   }
   next++;
}
